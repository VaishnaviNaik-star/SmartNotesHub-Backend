const express = require("express");
const mongoose = require("mongoose");
const dotenv = require("dotenv");
const cors = require("cors");
const path = require("path");
const seedDemoNotes = require("./seedDemoNotes");
const authRoutes = require("./routes/auth");
const notesRoutes = require("./routes/notes");
const uploadcareRoutes = require("./routes/uploadcare"); // âœ… FIXED: use require instead of import

// âœ… Load environment variables
dotenv.config();

const app = express();

// âœ… CORS Configuration
app.use(
  cors({
    origin: [
      "http://localhost:3000",
      "https://smartnoteshub-frontend.onrender.com",
    ],
    methods: ["GET", "POST", "PUT", "DELETE"],
    credentials: true,
  })
);

// âœ… Middleware
app.use(express.json());

// âœ… Static uploads (for demo files)
app.use("/api/uploads", express.static(path.join(__dirname, "uploads")));

// âœ… Routes
app.use("/api/auth", authRoutes);
app.use("/api/notes", notesRoutes);
app.use("/api/uploadcare", uploadcareRoutes); // âœ… Keep this below notes

// âœ… Root Route
app.get("/", (req, res) => {
  res.send("ðŸš€ Smart NotesHub Backend Running Successfully!");
});

// âœ… MongoDB + Seed
mongoose
  .connect(process.env.MONGO_URI)
  .then(async () => {
    console.log("âœ… MongoDB connected successfully!");
    await seedDemoNotes();
  })
  .catch((err) => console.error("âŒ MongoDB Connection Error:", err));

// âœ… Start Server
const PORT = process.env.PORT || 5000;
app.listen(PORT, () => {
  console.log(`ðŸš€ Server running on port ${PORT}`);
});
